package com.qentelli.automation.testdatafactory.data;

public class Product {
    private String productName;
    private String productDescription;
    private String quantity;
    private String retailPrice;
    private String yourCoachPrice;
    private String promotionalPrice;
    private String preferredCustomerPrice;
    private String pvPoints;
    private String productType;
    private String packagingSize;
    private String flavorType;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductDescription() {
        return productDescription;
    }

    public void setProductDescription(String productDescription) {
        this.productDescription = productDescription;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getRetailPrice() {
        return retailPrice;
    }

    public void setRetailPrice(String retailPrice) {
        this.retailPrice = retailPrice;
    }

    public String getYourCoachPrice() {
        return yourCoachPrice;
    }

    public void setYourCoachPrice(String yourCoachPrice) {
        this.yourCoachPrice = yourCoachPrice;
    }

    public String getPromotionalPrice() {
        return promotionalPrice;
    }

    public void setPromotionalPrice(String promotionalPrice) {
        this.promotionalPrice = promotionalPrice;
    }

    public String getPreferredCustomerPrice() {
        return preferredCustomerPrice;
    }

    public void setPreferredCustomerPrice(String preferredCustomerPrice) {
        this.preferredCustomerPrice = preferredCustomerPrice;
    }

    public String getPVPoints() {
        return pvPoints;
    }

    public void setPVPoints(String pvPoints) {
        this.pvPoints = pvPoints;
    }


    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public String getPackagingSize() {
        return packagingSize;
    }

    public void setPackagingSize(String packagingSize) {
        this.packagingSize = packagingSize;
    }

    public String getFlavorType() {
        return flavorType;
    }

    public void setFlavorType(String flavorType) {
        this.flavorType = flavorType;
    }
}
